#include "libft.h"

int		ft_isprint(int c)
{
	if ((ft_isalpha(c) == 1 || ft_isdigit(c) == 1 || (c >= 32 && c <= 47)
				|| (c >= 58 && c <= 63) || (c >= 91 && c <= 96)
				|| (c >= 123 && c <= 126)))
		return (1);
	else
		return (0);
}
